module.exports.Successfull=(res,mssg,data=null)=>{
  res.status(200).json({
    status:"Success",
    error:null,
    message:mssg,
    data:data
  })
}
module.exports.InternalServer=(res,mssg,data=null)=>{
  res.status(500).json({
    status:"Failed",
    message:mssg,
    data:data
  })
}
module.exports.Failed=(res,mssg,data=null)=>{
  res.status(400).json({
    status:"Bad Request",
    message:mssg,
    data:data
  })
}

module.exports.CustomError=(res,status,progress,mssg,errmessage,data)=>{
 res.status(status).json({
  status:progress,
  message:mssg,
  error:errmessage,
  data:data
  })
}